class DimensionScore(BaseModel):
    """
    Evaluation result for a single competency dimension.
    """

    name: str = Field(
        description="Name of the evaluated dimension."
    )

    score: float = Field(
        ge=0,
        le=100,
        description="Score assigned to the dimension."
    )

    justification: str = Field(
        description="Reasoning behind the assigned score."
    )


class PhaseEvaluationResult(BaseModel):
    """
    Evaluation result for a single interview phase.
    """

    phase_name: str = Field(
        description="Name of the evaluated phase."
    )

    dimensions: list[
        DimensionScore
    ] = Field(
        description="Scores for all evaluated dimensions."
    )

    overall_score: float = Field(
        ge=0,
        le=100,
        description="Aggregated phase score."
    )

    strengths: list[str] = Field(
        default_factory=list,
        description="Notable strengths."
    )

    improvements: list[str] = Field(
        default_factory=list,
        description="Improvement opportunities."
    )


