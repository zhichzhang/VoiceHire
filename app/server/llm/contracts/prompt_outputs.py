    clarity: float = Field(
        ge=0,
        le=1,
        description="How clearly and coherently the answer is expressed."
    )

    fluency: float = Field(
        ge=0,
        le=1,
        description="How smooth, natural, and readable the answer is."
    )

    confidence: float = Field(
        ge=0,
        le=1,
        description="How confident the model is in this assessment."
    )