class ResumeReviewResult(BaseModel):
    """
    Result returned by the resume ownership / update decision step.

    This is used before overwriting an existing candidate resume.

    Rules:
    - If `is_same_candidate` is False, keep the old resume untouched.
    - If `is_same_candidate` is True, proceed with resume normalization
      and update the existing resume record.
    """

    is_same_candidate: bool = Field(
        description=(
            "Whether the pasted resume belongs to the same candidate "
            "as the existing record."
        )
    )

    confidence: float = Field(
        ge=0,
        le=1,
        default=0.0,
        description=(
            "Confidence that the decision is correct."
        ),
    )

    reason: str = Field(
        default="",
        description=(
            "Short reason for the ownership decision."
        ),
    )

