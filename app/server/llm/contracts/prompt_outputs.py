from typing import Generic, TypeVar

from pydantic import BaseModel, Field

from app.server.models.interview_feedback import LLMInterviewFeedback

T = TypeVar("T")


# ============================================================
# Token Usage
# ============================================================

class TokenUsage(BaseModel):
    """
    Token consumption reported by an LLM provider.

    These metrics are provider-agnostic and are intended
    for:

    - Cost tracking
    - Usage analytics
    - Performance monitoring
    - Capacity planning

    Not all providers expose token usage information.
    Missing values should default to zero or be omitted.
    """

    prompt_tokens: int = Field(
        default=0,
        description=(
            "Number of input tokens consumed by "
            "the prompt."
        )
    )

    completion_tokens: int = Field(
        default=0,
        description=(
            "Number of output tokens generated "
            "by the model."
        )
    )

    total_tokens: int = Field(
        default=0,
        description=(
            "Total tokens consumed by the request."
        )
    )

# ============================================================
# Generic LLM Response
# ============================================================

class LLMResponse(BaseModel, Generic[T]):
    """
    Standardized structured response returned by an LLM.

    All prompt outputs may optionally be wrapped by this
    model to support future metadata expansion such as:

    - confidence
    - reasoning
    - provider information
    - token usage
    """

    result: T = Field(
        description="Structured result generated by the model."
    )

    token_usage: TokenUsage | None = Field(
        default=None,
        description="Token usage reported by the provider."
    )


# ============================================================
# Intro Phase Outputs
# ============================================================

class EducationContext(BaseModel):
    """
    Educational background highlighted by the candidate
    during the self-introduction phase.
    """

    institution: str = Field(
        description="School, university, bootcamp, or educational institution."
    )

    program: str | None = Field(
        default=None,
        description="Program, major, degree, or field of study."
    )

    status: str | None = Field(
        default=None,
        description=(
            "Current academic status such as "
            "'current student', 'graduated', "
            "'phd candidate', etc."
        )
    )


class HighlightedExperience(BaseModel):
    """
    Experience intentionally emphasized by the candidate
    during self-introduction.

    These experiences are later used for experience
    selection and deep-dive questioning.
    """

    organization: str | None = Field(
        default=None,
        description=(
            "Company, research lab, startup, school, "
            "or organization."
        )
    )

    timeframe: str | None = Field(
        default=None,
        description="Time period associated with the experience."
    )

    summary: str = Field(
        description="One-sentence summary of the experience."
    )

    responsibilities: list[str] = Field(
        default_factory=list,
        description="Responsibilities mentioned by the candidate."
    )

    achievements: list[str] = Field(
        default_factory=list,
        description="Achievements or impact mentioned by the candidate."
    )


class IntroExtractionResult(BaseModel):
    """
    Structured candidate context extracted from the
    self-introduction phase.

    This output is used by downstream phases for:

    - Experience selection
    - Follow-up question generation
    - Evaluation context construction
    """

    most_recent_role: str | None = Field(
        default=None,
        description="Most recent or current professional role."
    )

    education: EducationContext | None = Field(
        default=None,
        description="Most relevant educational background."
    )

    highlighted_experiences: list[
        HighlightedExperience
    ] = Field(
        default_factory=list,
        description="Experiences highlighted by the candidate."
    )

    domain_keywords: list[str] = Field(
        default_factory=list,
        description=(
            "Top domain, industry, or technical keywords "
            "associated with the candidate."
        )
    )

    other_context: list[str] = Field(
        default_factory=list,
        description="Additional contextual information."
    )


